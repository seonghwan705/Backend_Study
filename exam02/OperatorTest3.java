package exam02;

public class OperatorTest3 {

	public static void main(String[] args) {
		// 3. 비교 연산자 => 실행결과는 항상 "논리값"
		
		int a=10;
		int a2=5;
		System.out.println(a>a2);
		System.out.println(a>=a2);
		System.out.println(a<a2);
		System.out.println(a<=a2);
		System.out.println(a==a2);
		System.out.println(a!=a2);
//		System.out.println(a===a2); // JS의 identical 연산자는 지원안함
		
	}

}
